import Foundation
import UIKit
extension UIColor {
    class func meowBlueColor () -> UIColor {
        return UIColor(red: 0.23, green: 0.65, blue: 0.85, alpha: 1.0)
    }
    
    class func meowGreenColor () -> UIColor {
        return UIColor(red: 0.55, green: 0.74, blue: 0.39, alpha: 1.0)
    }
    
    class func meowYellowColor () -> UIColor {
        return UIColor(red: 0.99, green: 0.79, blue: 0.38, alpha: 1.0)
    }
    
    class func meowRedColor () -> UIColor {
        return UIColor(red: 0.88, green: 0.17, blue: 0.22, alpha: 1.0)
    }
    class func meowGreyColor () -> UIColor {
        return UIColor(red: 0.65, green: 0.66, blue: 0.67, alpha: 1.0)
    }
}